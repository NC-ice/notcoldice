#include<stdio.h>
/**
 * @brief 主函数，用于输出从1开始的奇数序列
 * @param void 无参数
 * @return int 返回0表示程序正常结束
 * 
 * 该函数首先读取一个整数n，然后输出从1开始到n为止的所有奇数，
 * 奇数之间用空格分隔。最后一个奇数后面不输出空格。
 */
int main(void)
{
    // 声明变量n用于存储用户输入的上限值
    int n;
    scanf("%d",&n);
    
    // 循环遍历从1开始的奇数，步长为2，直到超过n为止
    for(int i=1;i<=n;i+=2){
        printf("%d",i);
        
        // 判断当前奇数是否需要输出空格分隔符
        // 只有当当前数字不是最后一个需要输出的数字时才输出空格
        if(i<n-1){//输出空格 n-1
            printf(" ");
        }
    }
    return 0;
}