#include<stdio.h>
/**
 * @brief 打印乘法口诀表
 * 
 * 该函数接收一个整数N，然后打印出N*N的乘法口诀表。
 * 输出格式为：i*j=result，其中result左对齐占4个字符宽度。
 * 每一行显示从1*当前行数到当前行数*当前行数的乘法算式。
 * 
 * @param void 无参数
 * @return int 返回0表示程序正常结束
 */
int main(void)
{
    /* 变量声明：
     * N: 用户输入的数字，决定乘法表的大小
     * i: 内层循环变量，表示被乘数
     * sum: 存储乘法运算的结果
     * I: 外层循环变量，表示乘数
     */
    int N,i,sum,I;
    scanf("%d",&N);
    
    /* 外层循环控制乘数I从1到N */
    for(I=1;I<=N;I++){
        /* 内层循环控制被乘数i从1到I，实现三角形格式的乘法表 */
        for(i=1;i<=I;i++){
            sum=i*I;
            printf("%d*%d=%-4d",i,I,sum);
            /* 当被乘数等于乘数时，换行显示下一行 */
            if(i==I){
                printf("\n");
            }
        }
    }
}