#include<stdio.h>
/**
 * 主函数：生成并打印杨辉三角（帕斯卡三角形）
 * 
 * 该程序读入一个整数N，然后构造N行的杨辉三角，
 * 并按金字塔形状格式化输出到控制台。
 * 
 * 杨辉三角的规律：
 * - 每行的第一个和最后一个元素都是1
 * - 中间的元素等于上一行相邻两个元素之和
 * 
 * 参数: void - 无参数
 * 
 * 返回值: int - 程序执行状态，正常退出返回0
 */
int main(void)
{
    // 读取杨辉三角的行数N
    int N;
    scanf("%d",&N);
    
    // 创建二维数组存储杨辉三角，A[i][j]表示第i行第j列的数值
    int A[N][N];
    
    // 构造杨辉三角的数值
    for(int i=0;i<N;i++){
        // 每行首尾元素设为1
        A[i][0]=A[i][i]=1;
        
        // 对于第2行及以后的行，计算中间元素的值
        if(i>=2){
            // 计算当前行中间各元素的值（基于上一行的数值）
            for(int j=1;j<N-1;j++){
                A[i][j]=A[i-1][j]+A[i-1][j-1];
            }
            // 重新确保首尾元素为1
            A[i][0]=A[i][i]=1;
        }
    }
    
    // 格式化输出杨辉三角
    for(int i=0;i<N;i++){
        // 打印前导空格，实现居中对齐效果
        for(int j=1;j<N-i;j++){
            printf(" ");
        }
        
        // 打印当前行的所有数字，每个数字占4个字符宽度
        for(int j=0;j<i+1;j++){
            printf("%4d",A[i][j]);
        }
        
        // 换行，开始下一行的输出
        printf("\n");
    }
}