#include<stdio.h>
/**
 * @brief 计算给定日期是该年的第几天
 * 
 * 该函数读取用户输入的日期(格式为年/月/日)，判断是否为闰年，
 * 然后计算从该年1月1日到指定日期的总天数。
 * 
 * @param void 无参数
 * @return int 返回指定日期是该年的第几天
 */
int main(void)
{
    int year,month,day;
    // 读取用户输入的日期，格式为年/月/日
    scanf("%d/%d/%d",&year,&month,&day);
    
    // 判断是否为闰年：能被4整除且不能被100整除，或者能被400整除
    int isleapyear=(year%4==0&&year%100!=0)||year%400==0;
    
    // 定义每个月的天数数组，第一维表示月份，第二维[0]表示平年，[1]表示闰年
    int monthDays[13][2] = {
        {0, 0},   // 索引0无用，便于直接使用月份1-12
        {31, 31}, // 1月
        {28, 29}, // 2月（平年28天，闰年29天）
        {31, 31}, // 3月
        {30, 30}, // 4月
        {31, 31}, // 5月
        {30, 30}, // 6月
        {31, 31}, // 7月
        {31, 31}, // 8月
        {30, 30}, // 9月
        {31, 31}, // 10月
        {30, 30}, // 11月
        {31, 31}  // 12月
    };
    
    // 计算指定月份之前所有月份的天数总和
    int totaldays=0;
    for(int i=1;i<month;i++){
        totaldays=totaldays+monthDays[i][isleapyear];
    }
    
    // 加上当前月份已过的天数，得到总天数
    totaldays=totaldays+day;
    printf("%d",totaldays);
    
}//本题重要知识点在于如何使用二维数组，可以巧妙的解决闰年2月和各月不同天数的天数输出。