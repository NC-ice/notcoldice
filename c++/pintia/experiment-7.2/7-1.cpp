#include<stdio.h>
/**
 * 主函数
 * 该程序用于在一个二维数组中查找局部极大值点
 * 局部极大值点定义为：该元素大于其上下左右四个相邻元素的值
 * 
 * 程序流程：
 * 1. 读入二维数组的行数M和列数N
 * 2. 读入M×N个整数构成二维数组
 * 3. 遍历数组内部元素（不包括边界），查找局部极大值点
 * 4. 输出所有找到的局部极大值点及其位置
 * 5. 如果没有找到局部极大值点，则输出"None"和数组维度
 * 
 * @return int 返回0表示程序正常结束
 */
int main(void)
{
    /* 读取数组的行数M和列数N */
    int M,N;
    scanf("%d %d",&M,&N);
    
    /* 声明M行N列的二维数组 */
    int A[M][N];
    
    /* 读取二维数组的所有元素 */
    for(int i=0;i<M;i++){
        for(int j=0;j<N;j++){
            scanf("%d",&A[i][j]);
        }
    }
    
    /* 记录找到的局部极大值点个数 */
    int ifhave=0;
    
    /* 遍历数组内部元素，查找局部极大值点 */
    /* 只检查行列索引从1到M-2和N-2的元素，避免访问数组边界外的元素 */
    for(int i=1;i<M-1;i++){
        for(int j=1;j<N-1;j++){
            /* 判断当前元素是否大于其上、下、左、右四个相邻元素 */
            if(A[i][j]>A[i-1][j]&&A[i][j]>A[i][j-1]&&A[i][j]>A[i][j+1]&&A[i][j]>A[i+1][j]){
                /* 输出局部极大值点的值和位置(行号、列号) */
                printf("%d %d %d\n",A[i][j],i,j);
                ifhave++;
            }
        }
    }
    
    /* 如果没有找到局部极大值点，输出提示信息 */
    if(ifhave==0){
        printf("None %d %d",M,N);
    }
    return 0;
}