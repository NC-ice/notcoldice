#include<stdio.h>
/**
 * 主函数：计算指定范围内质数的个数和质数之和
 * 
 * 该函数从标准输入读取两个整数M和N，找出闭区间[M,N]内所有质数，
 * 统计质数个数并计算这些质数的总和，最后将结果输出到标准输出。
 * 
 * 参数：无
 * 
 * 返回值：int类型，程序执行状态
 *         0 - 正常执行结束
 */
int main(void)
{
    /* 声明变量：M和N为范围边界，count1用于统计因数个数，
       sum用于累计质数和，count2用于统计质数个数 */
    int M,N,count1=0,sum=0,count2=0;
    
    // 从标准输入读取范围的起始值M和结束值N
    scanf("%d %d",&M,&N);
    
    /* 遍历[M,N]范围内的每个数字，判断是否为质数 */
    for(int i=M;i<=N;i++){
        /* 内层循环：统计当前数字i的因数个数 */
        for(int I=1;I<=i;I++){
            if(i%I==0){
                count1++;
            }
        }
        
        /* 判断是否为质数：质数恰好有2个因数（1和自身） */
        if(count1==2){
                count2++;
                sum=sum+i;
        }
        
        // 重置因数计数器，为下一个数字的判断做准备
        count1=0;
    }
    
    // 输出质数个数和质数总和
    printf("%d %d",count2,sum);
}