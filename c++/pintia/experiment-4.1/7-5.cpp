#include<stdio.h>
#include<math.h>
/**
 * 主函数
 * 该程序读入两个整数a和n，计算一个由a组成的n位数的特殊求和
 * 
 * 参数: 无
 * 
 * 返回值: 
 *   0 - 程序正常执行结束
 * 
 * 功能说明:
 *   例如输入a=2, n=3，则计算2+22+222的值
 *   通过循环构造每一位的数字并累加到总和中
 */
int main(void)
{
    /* 声明变量：a为输入的数字，n为位数，sum为累加和 */
    int a,n,sum=0;
    
    /* 从标准输入读取两个整数a和n */
    scanf("%d %d",&a,&n);
    
    /* 循环构造并累加每一位的数字 */
    for(int i=0;i<n;i++){
        sum=a*(n-i)*pow(10,i)+sum;
    }
    
    /* 输出计算结果 */
    printf("s = %d",sum);
}